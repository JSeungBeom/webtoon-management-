package beans;

public class Detail {
	private String posttitle;
	private String postgenre;
	private String author;
	private String authorsay;
	private String postsummary;
	
	
	public String getPosttitle() {
		return posttitle;
	}
	public void setPosttitle(String posttitle) {
		this.posttitle = posttitle;
	}
	public String getPostgenre() {
		return postgenre;
	}
	public void setPostgenre(String postgenre) {
		this.postgenre = postgenre;
	}
	public String getAuthor() {
		return author;
	}
	public void setAuthor(String author) {
		this.author = author;
	}
	public String getAuthorsay() {
		return authorsay;
	}
	public void setAuthorsay(String authorsay) {
		this.authorsay = authorsay;
	}
	public String getPostsummary() {
		return postsummary;
	}
	public void setPostsummary(String postsummary) {
		this.postsummary = postsummary;
	}
	
	
}
